package com.eatitappclient.tws.Model;

public class SizeModel {
    private String name;
    private Double price;

    public SizeModel() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }
}
